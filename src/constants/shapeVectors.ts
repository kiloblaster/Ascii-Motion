// ── Normalization ─────────────────────────────────────────────────────────────

/**
 * Normalize shape vectors so that the max value in each dimension = 1.0.
 * This ensures the full range of characters is utilized during lookups.
 */
export function normalizeShapeVectors(
  entries: CharacterShapeEntry[]
): CharacterShapeEntry[] {
  if (entries.length === 0) return [];

  const dims = entries[0].vector.length;
  const maxPerDim = new Array(dims).fill(0);

  for (const entry of entries) {
    for (let i = 0; i < dims; i++) {
      if (entry.vector[i] > maxPerDim[i]) {
        maxPerDim[i] = entry.vector[i];
      }
    }
  }

  return entries.map(entry => ({
    char: entry.char,
    vector: entry.vector.map((v, i) =>
      maxPerDim[i] > 0 ? v / maxPerDim[i] : 0
    ) as [number, number, number, number, number, number],
  }));
}

