/**
 * Get normalized shape vectors for basic ASCII characters.
 * Generates accurate vectors from the browser's monospace font on first call,
 * falling back to the static estimates if canvas is unavailable.
 */
export function getNormalizedAsciiVectors(): CharacterShapeEntry[] {
  if (!_normalizedAscii) {
    _normalizedAscii = tryGenerateFromCanvas(PRINTABLE_ASCII)
      ?? normalizeShapeVectors(BASIC_ASCII_SHAPE_VECTORS);
  }
  return _normalizedAscii;
}

/**
 * Get normalized shape vectors for block/quadrant characters.
 * Generates accurate vectors from the browser's monospace font on first call,
 * falling back to the static estimates if canvas is unavailable.
 */
export function getNormalizedBlockVectors(): CharacterShapeEntry[] {
  if (!_normalizedBlocks) {
    _normalizedBlocks = tryGenerateFromCanvas(BLOCK_CHARACTERS)
      ?? normalizeShapeVectors(BLOCK_CHAR_SHAPE_VECTORS);
  }
  return _normalizedBlocks;
}

/**
 * Attempt to generate shape vectors by rendering characters on a canvas.
 * Returns null if not running in a browser environment.
 */
function tryGenerateFromCanvas(characters: string[]): CharacterShapeEntry[] | null {
  if (typeof document === 'undefined') return null;
  try {
    return generateShapeVectors(characters);
  } catch {
    return null;
  }
}

// ── Browser-based shape vector generator ──────────────────────────────────────

/**
 * Generate shape vectors by rendering characters on a canvas.
 * This produces exact vectors for the current browser's monospace font.
 * Call this in a browser environment to regenerate or verify the precomputed vectors.
 *
 * @param characters - Array of single characters to compute vectors for
 * @param fontFamily - Monospace font to use (default: 'monospace')
 * @param fontSize - Font size in pixels (default: 48 for precision)
 * @param samplesPerCircle - Number of sample points per sampling circle (default: 64)
 */
export function generateShapeVectors(
  characters: string[],
  fontFamily: string = 'monospace',
  fontSize: number = 48,
  samplesPerCircle: number = 64
): CharacterShapeEntry[] {
  // Cell dimensions for the font (monospace chars are typically ~0.6 width:height)
  const cellWidth = Math.ceil(fontSize * 0.6);
  const cellHeight = fontSize;

  const canvas = document.createElement('canvas');
  canvas.width = cellWidth;
  canvas.height = cellHeight;
  const ctx = canvas.getContext('2d')!;

  const entries: CharacterShapeEntry[] = [];

  for (const char of characters) {
    // Clear canvas (black background)
    ctx.fillStyle = '#000000';
    ctx.fillRect(0, 0, cellWidth, cellHeight);

    // Draw character in white
    ctx.fillStyle = '#FFFFFF';
    ctx.font = `${fontSize}px ${fontFamily}`;
    ctx.textBaseline = 'top';
    ctx.textAlign = 'center';
    ctx.fillText(char, cellWidth / 2, 0);

    const imageData = ctx.getImageData(0, 0, cellWidth, cellHeight);
    const pixels = imageData.data;

    // Sample each of the 6 internal circles
    const vector: [number, number, number, number, number, number] = [0, 0, 0, 0, 0, 0];

    for (let ci = 0; ci < INTERNAL_CIRCLES.length; ci++) {
      const [cx, cy, cr] = INTERNAL_CIRCLES[ci];
      const centerPx = cx * cellWidth;
      const centerPy = cy * cellHeight;
      const radiusPx = cr * Math.min(cellWidth, cellHeight);

      let hits = 0;
      let total = 0;

      // Grid-sample within the circle
      const step = (radiusPx * 2) / Math.sqrt(samplesPerCircle);
      for (let sy = centerPy - radiusPx; sy <= centerPy + radiusPx; sy += step) {
        for (let sx = centerPx - radiusPx; sx <= centerPx + radiusPx; sx += step) {
          const dx = sx - centerPx;
          const dy = sy - centerPy;
          if (dx * dx + dy * dy > radiusPx * radiusPx) continue;

          const px = Math.floor(sx);
          const py = Math.floor(sy);
          if (px < 0 || px >= cellWidth || py < 0 || py >= cellHeight) continue;

          total++;
          const idx = (py * cellWidth + px) * 4;
          // Use red channel (white text = high value)
          if (pixels[idx] > 128) hits++;
        }
      }

      vector[ci] = total > 0 ? hits / total : 0;
    }

    entries.push({ char, vector });
  }

  return normalizeShapeVectors(entries);
}
