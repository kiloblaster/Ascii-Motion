/**
 * Precomputed 6D shape vectors for shape-based ASCII rendering.
 *
 * Each character is represented by a 6-dimensional vector capturing its visual
 * density across 6 sub-regions (sampling circles) arranged in a 2×3 staggered grid:
 *
 *   [0] [1]   ← upper row
 *   [2] [3]   ← middle row
 *   [4] [5]   ← lower row
 *
 * Left column circles are shifted slightly down, right column slightly up,
 * to minimize gaps. See: https://alexharri.com/blog/ascii-rendering
 */

